public class latihan{
	public static void main(String args[]){
		int test=5;
		System.out.println("test : "+ test++);
		System.out.println("\ntest++");
		System.out.println(test+"\n");
		
		System.out.println("test :"+ test--);
		System.out.println("\ntest--");
		System.out.println("test : "+ test);
	}
}